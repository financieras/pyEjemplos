ciudad="Buenos Aires"
print(ciudad[0])       #B  se comienza por el índice=0
print(ciudad[1])       #u
print(ciudad[2])       #e
print(ciudad[3])       #n
print(ciudad[-1])      #s
#slicing (métodos de corte)
print(ciudad[0:4])     #Buen
print(ciudad[:4])      #Buen
print(ciudad[7:10])    #Air
print(ciudad[:])       #Buenos Aires
print(ciudad[2:9:2])   #eo i
print(ciudad[::-1])    #seriA soneuB

